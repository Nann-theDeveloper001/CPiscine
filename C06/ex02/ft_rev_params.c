#include<unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	y;

	y = 1;
	while (argc > y)
	{
		i = 0;
		while (argv[argc - 1][i])
		{
			write(1, &argv[argc - 1][i], 1);
			i++;
		}
		argc--;
		write(1, "\n", 1);
	}
}
