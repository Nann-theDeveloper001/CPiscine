#include<stdio.h>
#include<unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	y;

	y = 1;
	while (y < argc)
	{	
		i = 0;
		while (argv[y][i])
		{
			write(1, &argv[y][i], 1);
			i++;
		}
		y++;
		write(1, "\n", 1);
	}
}
