#include<unistd.h>

int	ft_str_is_lowercase(char *str)
{	
	int	flag;
	int	i;

	i = 0;
	flag = 1;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			flag = 1;
		}
		else
		{
			return (0);
		}
		i++;
	}
	return (flag);
}
